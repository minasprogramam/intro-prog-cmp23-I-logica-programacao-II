// Listas de compras em diferentes arrays
let lista1 = ["Maçã", "Banana", "Laranja"];
let lista2 = ["Leite", "Pão", "Queijo"];
let lista3 = ["Arroz", "Feijão", "Carne"];

// Mesclando as listas usando o método concat()
let listaFinal = lista1.concat(lista2, lista3);

// Imprimindo a lista resultante na tela
console.log(listaFinal);
