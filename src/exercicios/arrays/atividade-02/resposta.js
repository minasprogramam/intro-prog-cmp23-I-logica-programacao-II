// Criando o array fila com os nomes das pessoas na fila
let fila = ["May", "Sol", "Elis", "Miriam"];

// Adicionando mais uma pessoa ao final da fila usando o método push
fila.push("Dai");

// Removendo a primeira pessoa da fila usando o método pop
fila.pop();

// Imprimindo o array resultante
console.log(fila);
